def hori_Histogram(p_Count,r_Count,t_Count,e_Count,out_Total):
    print("----------------------------------------------------------------")
    print("Horizonal Histogram")
    print("progress ",p_Count,"\t:","*" * p_Count)
    print("retriever",r_Count,"\t:","*" * r_Count)
    print("trailer  ",t_Count,"\t:","*" * t_Count)
    print("excluded ", e_Count,"\t:","*" * e_Count)
    print("\n")
    print(out_Total," outcomes in total.")
    
